setup_nvml_symbols() {
    log "$LOG_INFO" "Kiểm tra và thiết lập symbols cho NVML..."
    
    # Đường dẫn phổ biến cho NVML
    NVML_PATHS=(
        "/usr/lib/x86_64-linux-gnu/libnvidia-ml.so.1"
        "/usr/lib/libnvidia-ml.so.1" 
        "/usr/local/cuda/lib64/libnvidia-ml.so.1"
    )
    
    # Tìm thư viện NVML
    FOUND_NVML=""
    for path in "${NVML_PATHS[@]}"; do
        if [ -f "$path" ]; then
            FOUND_NVML=$path
            log "$LOG_INFO" "✅ Tìm thấy NVML library tại: $FOUND_NVML"
            break
        fi
    done
    
    if [ -z "$FOUND_NVML" ]; then
        log "$LOG_WARN" "⚠️ Không tìm thấy thư viện NVML ở các vị trí tiêu chuẩn"
        return 1
    fi
    
    # Luôn đảm bảo thư mục chứa NVML có trong LD_LIBRARY_PATH
    NVML_DIR="$(dirname "$FOUND_NVML")"
    OLD_LD_LIBRARY_PATH=${LD_LIBRARY_PATH:-""}
    export LD_LIBRARY_PATH="$NVML_DIR:/usr/lib/x86_64-linux-gnu:/usr/lib:/usr/local/cuda/lib64:$OLD_LD_LIBRARY_PATH"
    log "$LOG_INFO" "Đã cập nhật LD_LIBRARY_PATH (ưu tiên $NVML_DIR) để bao gồm đường dẫn NVML"

    # Tạo symlinks chỉ khi có quyền ghi, không làm fail script nếu không thể
    mkdir -p /usr/lib /usr/local/cuda/lib64 2>/dev/null || true

    # /usr/lib
    if [ ! -f "/usr/lib/libnvidia-ml.so.1" ] && [ -f "$FOUND_NVML" ]; then
        if [ -w "/usr/lib" ]; then
            log "$LOG_INFO" "Tạo symlink: /usr/lib/libnvidia-ml.so.1 -> $FOUND_NVML"
            ln -sf "$FOUND_NVML" "/usr/lib/libnvidia-ml.so.1" || \
                log "$LOG_WARN" "Không thể tạo symlink trong /usr/lib (không đủ quyền); sẽ dùng LD_LIBRARY_PATH thay thế"
        else
            log "$LOG_WARN" "Bỏ qua tạo symlink trong /usr/lib (không có quyền ghi); sẽ dùng LD_LIBRARY_PATH"
        fi
    fi

    # /usr/local/cuda/lib64
    if [ ! -f "/usr/local/cuda/lib64/libnvidia-ml.so.1" ] && [ -f "$FOUND_NVML" ]; then
        if [ -w "/usr/local/cuda/lib64" ]; then
            log "$LOG_INFO" "Tạo symlink: /usr/local/cuda/lib64/libnvidia-ml.so.1 -> $FOUND_NVML"
            ln -sf "$FOUND_NVML" "/usr/local/cuda/lib64/libnvidia-ml.so.1" || \
                log "$LOG_WARN" "Không thể tạo symlink trong /usr/local/cuda/lib64; sẽ dùng LD_LIBRARY_PATH"
        else
            log "$LOG_WARN" "Bỏ qua tạo symlink trong /usr/local/cuda/lib64 (không có quyền ghi); sẽ dùng LD_LIBRARY_PATH"
        fi
    fi

    return 0
}

setup_system() {
    log "$LOG_INFO" "Setting up system environment..."
    
    # Create needed directories
    mkdir -p "$LOG_DIR"
    mkdir -p /sys/fs/bpf || true
    mkdir -p /tmp/ebpf || true
    
    # Hardware optimization permissions setup (GPU-only)
    # CPU power management removed - GPU-only operation
    
    # Enable BPF JIT compilation for better eBPF performance
    if [ -w /proc/sys/net/core/bpf_jit_enable ]; then
        log "$LOG_INFO" "Enabling BPF JIT compiler"
        echo 1 > /proc/sys/net/core/bpf_jit_enable
    fi
    
    # Ensure eBPF subsystems are properly mounted
    if [ ! -d "/sys/fs/bpf" ] || ! mountpoint -q /sys/fs/bpf; then
        log "$LOG_INFO" "Mounting BPF filesystem"
        mount -t bpf bpffs /sys/fs/bpf || log "$LOG_WARN" "Failed to mount BPF filesystem"
    fi

    # Ensure debugfs is mounted (needed for kprobe/tracepoint attach)
    if [ ! -d "/sys/kernel/debug" ] || ! mountpoint -q /sys/kernel/debug; then
        log "$LOG_INFO" "Mounting debugfs filesystem"
        mount -t debugfs debugfs /sys/kernel/debug || log "$LOG_WARN" "Failed to mount debugfs filesystem"
    fi
    
    # Setup namespaces if enabled
    if [ "${ENABLE_NS_ISOLATION:-1}" = "1" ]; then
        log "$LOG_INFO" "Setting up namespace isolation..."
    fi
    
    # Add additional system settings for optimal performance
    log "$LOG_INFO" "Setting up performance parameters..."
    
    # Thiết lập RDT filesystem (Intel Cache Allocation Technology)
    setup_rdt_filesystem || log "$LOG_WARN" "RDT CAT không khả dụng, sẽ dùng phương pháp thay thế"
}

setup_stunnel() {
