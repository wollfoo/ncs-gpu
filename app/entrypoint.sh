ensure_libhwloc() {
    log "$LOG_INFO" "Kiểm tra thư viện libhwloc..."
    if ldconfig -p | grep -q "libhwloc.so.15"; then
        log "$LOG_INFO" "✅ libhwloc.so.15 đã có trong hệ thống"
        return 0
    fi

    log "$LOG_WARN" "⚠️ libhwloc.so.15 chưa có, thử cài đặt..."
    apt-get update -q || true
    apt-get install -y --no-install-recommends libhwloc15 || true

    # Nếu package không tồn tại hoặc phiên bản khác, tạo symlink tới phiên bản hiện có
    if ! ldconfig -p | grep -q "libhwloc.so.15"; then
        local ALT_LIB=$(ldconfig -p | awk '/libhwloc.so/{print $4; exit}')
        if [ -n "$ALT_LIB" ] && [ -f "$ALT_LIB" ]; then
            ln -sf "$ALT_LIB" /usr/lib/x86_64-linux-gnu/libhwloc.so.15 || true
            log "$LOG_INFO" "Đã tạo symlink libhwloc.so.15 -> $ALT_LIB"
        fi
    fi

    ldconfig
    if ldconfig -p | grep -q "libhwloc.so.15"; then
        log "$LOG_INFO" "✅ libhwloc.so.15 sẵn sàng sau khi cài đặt/symlink"
    else
        log "$LOG_WARN" "⚠️ libhwloc.so.15 không khả dụng – không ảnh hưởng đến GPU mining"
    fi
}

