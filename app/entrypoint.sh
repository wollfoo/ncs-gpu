    log "$LOG_INFO" "Thiết lập Intel RDT Cache Allocation Technology..."
    
    # Chạy script thiết lập RDT nếu tồn tại
    if [ -f "/app/setup_rdt.sh" ]; then
        chmod +x /app/setup_rdt.sh
        /app/setup_rdt.sh
        
        if [ $? -eq 0 ]; then
            log "$LOG_INFO" "✅ RDT CAT đã được thiết lập thành công"
            return 0
        else
            log "$LOG_WARN" "⚠️ Không thể thiết lập RDT CAT, plugin sẽ bị vô hiệu hóa"
            # Đã xử lý trong script setup_rdt.sh
