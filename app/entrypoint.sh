check_hostpid_and_ulimits() {
    # Generated by Copilot
    # 1) Phát hiện hostPID=false (PID namespace riêng) – eBPF tracepoint không hoạt động
    # Cải thiện kiểm tra PID namespace với nhiều phương pháp
    local hostpid_detected=false
    
    # Phương pháp 1: Kiểm tra inode của PID namespace
    local init_ns="$(stat -c %i /proc/1/ns/pid 2>/dev/null || echo '')"
    local self_ns="$(stat -c %i /proc/self/ns/pid 2>/dev/null || echo '')"
    
    # Phương pháp 2: Kiểm tra xem PID 1 có phải systemd/init không
    local pid1_comm="$(cat /proc/1/comm 2>/dev/null || echo '')"
    
    # Phương pháp 3: Kiểm tra mount namespace (container thường có mount namespace riêng)
    local host_processes="$(ps aux | grep -v docker | grep -v containerd | wc -l 2>/dev/null || echo '0')"
    
    # Xác định hostPID dựa trên multiple checks
    if [[ -n "$init_ns" && -n "$self_ns" && "$init_ns" == "$self_ns" ]]; then
        hostpid_detected=true
        log "$LOG_INFO" "✅ Phát hiện hostPID=true qua PID namespace inode check"
    elif [[ "$pid1_comm" == "systemd" || "$pid1_comm" == "init" ]]; then
        hostpid_detected=true
        log "$LOG_INFO" "✅ Phát hiện hostPID=true qua PID 1 comm check ($pid1_comm)"
    elif [[ "$host_processes" -gt 50 ]]; then
        hostpid_detected=true
        log "$LOG_INFO" "✅ Phát hiện hostPID=true qua process count check ($host_processes processes)"
    fi
    
    # Chỉ báo lỗi nếu tất cả checks đều thất bại
    if [[ "$hostpid_detected" == "false" ]]; then
        log "$LOG_WARN" "⚠️  Không thể xác nhận hostPID=true - eBPF sched_switch tracepoint có thể không hoạt động tối ưu."
        log "$LOG_WARN" "� Khuyến nghị: Chạy container với --pid=host hoặc đặt hostPID: true trong PodSpec."
        # Không exit, chỉ cảnh báo để cho phép container tiếp tục chạy
    fi

    # 2) Tự nới memlock để bpf_map_pin_to_user không thất bại
    if command -v prlimit >/dev/null 2>&1; then
        prlimit --pid $$ --memlock unlimited 2>/dev/null || \
            log "$LOG_WARN" "Không thể nới memlock – thiếu CAP_SYS_RESOURCE; hãy thêm --ulimit memlock=-1:" 
    fi

    # 3) Kiểm tra perf_event_paranoid, disable IPC-sampling nếu kernel hạn chế
    local pep="$(cat /proc/sys/kernel/perf_event_paranoid 2>/dev/null || echo '3')"
    if [[ $pep -gt 2 ]]; then
        export DISABLE_IPC_SAMPLING=1
        log "$LOG_WARN" "perf_event_paranoid=$pep (>2) – đã đặt DISABLE_IPC_SAMPLING=1 để tắt IPC sampling."
    fi
}

