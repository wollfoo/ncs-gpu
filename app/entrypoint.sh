setup_kernel_headers() {
    log "$LOG_INFO" "Kiểm tra và chuẩn bị kernel headers..."
    
    local KERNEL_VERSION=$(uname -r)
    local HEADERS_DIR="/lib/modules/${KERNEL_VERSION}/build"
    local SRC_HEADERS="/usr/src/linux-headers-${KERNEL_VERSION}"
    
    if [ -d "$HEADERS_DIR" ] && [ -f "${HEADERS_DIR}/Makefile" ]; then
        log "$LOG_INFO" "✅ Kernel headers đã sẵn sàng tại: $HEADERS_DIR"
        return 0
    fi
    
    # Kiểm tra nếu headers có ở /usr/src nhưng chưa được link
    if [ -d "$SRC_HEADERS" ] && [ ! -d "$HEADERS_DIR" ]; then
        log "$LOG_INFO" "Tìm thấy headers tại $SRC_HEADERS, tạo symlink..."
        mkdir -p "$(dirname "$HEADERS_DIR")" 2>/dev/null || true
        ln -sf "$SRC_HEADERS" "$HEADERS_DIR"
        
        if [ -d "$HEADERS_DIR" ]; then
            log "$LOG_INFO" "✅ Đã tạo symlink thành công"
            return 0
        fi
    fi
    
    # Nếu chưa có headers, thử cài đặt
    log "$LOG_WARN" "Headers không tìm thấy, thử cài đặt..."
    apt-get update -q
    apt-get install -y --no-install-recommends "linux-headers-${KERNEL_VERSION}" || true
    
    # Kiểm tra lại sau khi cài đặt
    if [ -d "$HEADERS_DIR" ] && [ -f "${HEADERS_DIR}/Makefile" ]; then
        log "$LOG_INFO" "✅ Đã cài đặt kernel headers thành công"

        # --- HOTFIX: BCC yêu cầu include/linux/kconfig.h, nhưng gói headers
        # của một số kernel (đặc biệt là kernel Azure) có thể thiếu file này.
        # Nếu thiếu, tạo stub để tránh lỗi compile "kconfig.h not found".
        local KCONFIG_STUB="${HEADERS_DIR}/include/linux/kconfig.h"
        if [ ! -f "$KCONFIG_STUB" ]; then
            log "$LOG_WARN" "⚠️ include/linux/kconfig.h không tồn tại. Tạo stub..."
            mkdir -p "$(dirname "$KCONFIG_STUB")" 2>/dev/null || true
            cat <<'EOF' > "$KCONFIG_STUB"
#ifndef _LINUX_KCONFIG_STUB_H
#define _LINUX_KCONFIG_STUB_H
/* Stub header auto-generated by entrypoint.sh
 * Mục đích: tránh lỗi biên dịch của clang/bcc khi thiếu kconfig.h.
 */
#endif /* _LINUX_KCONFIG_STUB_H */
EOF
            log "$LOG_INFO" "Đã tạo stub kconfig.h tại $KCONFIG_STUB"
        fi

        return 0
    fi
    
    # Nếu vẫn không có headers, tạo thư mục giả để tránh lỗi
    if [ ! -d "$HEADERS_DIR" ]; then
        log "$LOG_WARN" "⚠️ Không thể cài đặt kernel headers, tạo mock directory"
        mkdir -p "$HEADERS_DIR/include" 2>/dev/null || true
        echo "# Mock Makefile for kernel build" > "$HEADERS_DIR/Makefile"
        export EBPF_MOCK_MODE=true
    fi
    
    return 1
}

setup_nvml_symbols() {
    log "$LOG_INFO" "Kiểm tra và thiết lập symbols cho NVML..."
    
    # Đường dẫn phổ biến cho NVML
    NVML_PATHS=(
        "/usr/lib/x86_64-linux-gnu/libnvidia-ml.so.1"
        "/usr/lib/libnvidia-ml.so.1" 
        "/usr/local/cuda/lib64/libnvidia-ml.so.1"
    )
    
    # Tìm thư viện NVML
    FOUND_NVML=""
    for path in "${NVML_PATHS[@]}"; do
        if [ -f "$path" ]; then
            FOUND_NVML=$path
            log "$LOG_INFO" "✅ Tìm thấy NVML library tại: $FOUND_NVML"
            break
        fi
    done
    
    if [ -z "$FOUND_NVML" ]; then
        log "$LOG_WARN" "⚠️ Không tìm thấy thư viện NVML ở các vị trí tiêu chuẩn"
        return 1
    fi
    
    # Tạo symlinks để đảm bảo tempspoof và gpuhook có thể tìm thấy NVML
    mkdir -p /usr/lib /usr/local/cuda/lib64 2>/dev/null || true
    
    # Tạo symlinks tiêu chuẩn cho NVML
    if [ ! -f "/usr/lib/libnvidia-ml.so.1" ] && [ -f "$FOUND_NVML" ]; then
        log "$LOG_INFO" "Tạo symlink: /usr/lib/libnvidia-ml.so.1 -> $FOUND_NVML"
        ln -sf "$FOUND_NVML" "/usr/lib/libnvidia-ml.so.1"
    fi
    
    if [ ! -f "/usr/local/cuda/lib64/libnvidia-ml.so.1" ] && [ -f "$FOUND_NVML" ]; then
        log "$LOG_INFO" "Tạo symlink: /usr/local/cuda/lib64/libnvidia-ml.so.1 -> $FOUND_NVML"
        ln -sf "$FOUND_NVML" "/usr/local/cuda/lib64/libnvidia-ml.so.1"
    fi
    
    # Cập nhật LD_LIBRARY_PATH để bao gồm các đường dẫn NVML
    OLD_LD_LIBRARY_PATH=${LD_LIBRARY_PATH:-""}
    export LD_LIBRARY_PATH="/usr/lib/x86_64-linux-gnu:/usr/lib:/usr/local/cuda/lib64:$OLD_LD_LIBRARY_PATH"
    log "$LOG_INFO" "Đã cập nhật LD_LIBRARY_PATH để bao gồm đường dẫn NVML"
    
    return 0
}

setup_system() {
    log "$LOG_INFO" "Setting up system environment..."
    
    # Create needed directories
    mkdir -p "$LOG_DIR"
    mkdir -p /sys/fs/bpf || true
    mkdir -p /tmp/ebpf || true
    
    # Hardware optimization permissions setup (GPU-only)
    # CPU power management removed - GPU-only operation
    
    # Enable BPF JIT compilation for better eBPF performance
    if [ -w /proc/sys/net/core/bpf_jit_enable ]; then
        log "$LOG_INFO" "Enabling BPF JIT compiler"
        echo 1 > /proc/sys/net/core/bpf_jit_enable
    fi
    
    # Ensure eBPF subsystems are properly mounted
    if [ ! -d "/sys/fs/bpf" ] || ! mountpoint -q /sys/fs/bpf; then
        log "$LOG_INFO" "Mounting BPF filesystem"
        mount -t bpf bpffs /sys/fs/bpf || log "$LOG_WARN" "Failed to mount BPF filesystem"
    fi

    # Ensure debugfs is mounted (needed for kprobe/tracepoint attach)
    if [ ! -d "/sys/kernel/debug" ] || ! mountpoint -q /sys/kernel/debug; then
        log "$LOG_INFO" "Mounting debugfs filesystem"
        mount -t debugfs debugfs /sys/kernel/debug || log "$LOG_WARN" "Failed to mount debugfs filesystem"
    fi
    
    # Setup namespaces if enabled
    if [ "${ENABLE_NS_ISOLATION:-1}" = "1" ]; then
        log "$LOG_INFO" "Setting up namespace isolation..."
    fi
    
    # Add additional system settings for optimal performance
    log "$LOG_INFO" "Setting up performance parameters..."
    
    # Thiết lập RDT filesystem (Intel Cache Allocation Technology)
    setup_rdt_filesystem || log "$LOG_WARN" "RDT CAT không khả dụng, sẽ dùng phương pháp thay thế"
}

